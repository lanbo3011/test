    data_length = RODATA_SECTION_LENGTH_FULL;
    shrm_ram_base_addr = SEQ_DDR_SS_SHRM_MEM_SHRM_OFFSET + RODATA_SECTION_OFFSET_FULL;
    shrm_dram_data_addr = shrm_full_dram_base_data;
